// C++ program to concate string
#include <iostream>
#include <string> //if we add string in our program then we  have to add this library
using namespace std;
int main()
{
    string firstname, lastname;
    firstname = "Shubhranshu";
    lastname = "Bhardwaj";
    cout << firstname + " " + lastname; // concate means adding two string
    return 0;
}