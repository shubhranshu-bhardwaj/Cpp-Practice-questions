//C++ Program to Find Quotient and Remainder take input from user

#include<iostream>
using namespace std;
int main(){
    int x,y;
    cout<<"Enter first no.:";
    cin>>x;
    cout<<"Enter second no.:";
    cin>>y;
    cout<<"Quotient:"<<x/y<<endl;
    cout<<"Remainder:"<<x%y;
    return 0;
}